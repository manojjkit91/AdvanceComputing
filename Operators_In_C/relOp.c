/**
 * To demonstrate the relational operators
 */
#include<stdio.h>
int main ( int argc, char **argv )
{
   int x = 10;
   int y = 12;
   /* For x < y */
   if ( x < y )
   {
       printf( " x < y : TRUE\n" );
   }
   else 
   {
       printf( " x < y : FALSE\n" );
   }
   /* For x <= y */
   if ( x <= y )
   {
       printf( " x <= y : TRUE\n" );
   }
   else 
   {
       printf( " x <= y : FALSE\n" );
   }
   /* For x > y */
   if ( x > y )
   {
       printf( " x > y : TRUE\n" );
   }
   else 
   {
       printf( " x > y : FALSE\n" );
   }
   /* For x >= y */
   if ( x >= y )
   {
       printf( " x >= y : TRUE\n" );
   }
   else 
   {
       printf( " x >= y : FALSE\n" );
   }
   /* For x == y */
   if ( x < y )
   {
       printf( " x == y : TRUE\n" );
   }
   else 
   {
       printf( " x == y : FALSE\n" );
   }
   /* x != y */
   if ( x != y )
   {
       printf( " x != y : TRUE\n" );
   }
   else 
   {
       printf( " x != y : FALSE\n" );
   }
   return 0;
}
